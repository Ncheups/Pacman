void draw_maze()
{
    for(int x =0;x<COLUMNS;x++)
    {
        for(int y=0;y<ROWS;y++)
        {
           switch(maze[x][y])
           {
            case 0: drawComponent(x,y,Wall);break;
            case 1: drawComponent(x,y,Cell);break;
            case 2: drawComponent(x,y,Cell);break;
            case 3: drawComponent(x,y,Gate);break;
            case 4: drawComponent(x,y,Portal);break;
           }
        }
    }
}

int getState(int x, int y)
{
    return maze[x][y];
}
